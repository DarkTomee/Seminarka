    private void btOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOkActionPerformed
        int max = inventarService.pocetPredmetu() - 1;
                
        if (tfIndexPolozky.getText().isEmpty()){
            taKonzole.append("\nZadej číslo předmětu");
        } else if (utils.Vstupy.isInt(tfIndexPolozky.getText())== false){
            taKonzole.append("\nNesprávný formát čísla předmětu");
        } else if (Integer.parseInt(tfIndexPolozky.getText()) > max || Integer.parseInt(tfIndexPolozky.getText()) < 0 ){
            taKonzole.append("\nŠpatné číslo předmětu předmětu");
        } else{
            
            PolozkaInventare predmet = new PolozkaInventare();
            index = Integer.parseInt(tfIndexPolozky.getText());
            predmet = inventarService.vyberPolozku(index);
            
            tfCenaEdit.setText(Double.toString(predmet.getCena()));
            tfNazevEdit.setText(predmet.getNazev());
            tfIndexPolozky.setVisible(false);
            laEdit.setVisible(false);
            btOk.setVisible(false);
            
            btUpravit.setVisible(true);
            btProdat.setVisible(true);
            laCenaEdit.setVisible(true);
            tfCenaEdit.setVisible(true);
            tfNazevEdit.setVisible(true);
            laNazevEdit.setVisible(true);
        }
    }//GEN-LAST:event_btOkActionPerformed
