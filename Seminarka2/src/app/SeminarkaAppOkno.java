    private void btUtekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUtekActionPerformed
        boolean utek = false;
        utek = model.VypravaOkno.utek(potvora, hrdina);
        
        if (utek == true){
            utek = false;
            zobrazMenu(hrdina);
        } else {
            model.VypravaOkno.rozhodnuti(potvora, hrdina);
        }
    }//GEN-LAST:event_btUtekActionPerformed
    private void btVyrobitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btVyrobitActionPerformed
        String nazev = null;
        double cena = 0;
        
        if (tfNazev.getText().isEmpty()){
            taKonzole.append("\nZadejte název předmětu.");
        } else if (tfCena.getText().isEmpty()){
            taKonzole.append("\nZadejte cenu předmětu.");
        } else if (utils.Vstupy.isDouble(tfCena.getText())== false){
            taKonzole.append("\nZadejte cenu předmětu.[číslo]");
        } else {
            nazev = tfNazev.getText();
            cena = Double.parseDouble(tfCena.getText());
            inventarService.pridejPolozku(nazev, cena);
            taKonzole.append("\nPředmět " + nazev + " v hodnotě: " + cena + " zlatých byl přidán do inventáře.\n");
        
        laCena.setVisible(false);
        laNazev.setVisible(false);
        tfCena.setVisible(false);
        tfNazev.setVisible(false);
        btVyrobit.setVisible(false);
        tfVyhledavaciPole.setVisible(false);
        btHledejPolozku.setVisible(false);
        
        paEdit.setVisible(false);
        btEdit.setVisible(false);
        }
    }//GEN-LAST:event_btVyrobitActionPerformed
